package com.labs.java.fp;

public class CalcOOP {

    private int a;
    private int b;

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int add() {
        return this.a + this.b;
    }

    public int substract() {
        return this.a - this.b;
    }

    public int multiply() {
        return this.a * this.b;
    }

    public int divide() {
        return this.a - this.b;
    }
}
